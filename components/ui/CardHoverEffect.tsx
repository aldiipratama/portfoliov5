"use client"

import { cn } from "@/lib/utils";
import { AnimatePresence, motion } from "framer-motion"
import Link from "next/link";
import { ReactElement, useState } from "react"

export const HoverEffect = ({
  items,
  className
}: {
  items: {
    title: string;
    icon: ReactElement;
    link: string;
  }[];
  className?: string;
}) => {
  let [hoveredIndex, setHoveredIndex] = useState<number | null>(null);

  return (
    <div
      className={cn(
        "grid grid-cols-2 sm:grid-cols-3 md:grid-cols-5 gap-4 lg:grid-cols-7 py-10 justify-evenly",
        className
      )}
    >
      {items.map((item, idx) => (
        <div
          key={idx}
          className="relative group block py-5 h-full w-full"
          onMouseEnter={() => setHoveredIndex(idx)}
          onMouseLeave={() => setHoveredIndex(null)}
        >
          <AnimatePresence>
            {hoveredIndex === idx && (
              <motion.span
                className="absolute inset-0 h-full w-full bg-neutral-200 dark:bg-slate-800/[0.8] block rounded-3xl"
                layoutId="hoverBackground"
                initial={{ opacity: 0 }}
                animate={{
                  opacity: 1,
                  transition: { duration: 0.15 },
                }}
                exit={{
                  opacity: 0,
                  transition: { duration: 0.15, delay: 0.2 },
                }}
              />
            )}
          </AnimatePresence>
          <PinContainer title={item.title} href={item.link}>
            {item.icon}
          </PinContainer>
        </div>
      ))}
    </div>
  );
};

export const PinContainer = ({
  children,
  title,
  href,
  className,
  containerClassName,
}: {
  children: React.ReactNode;
  title?: string;
  href: string;
  className?: string;
  containerClassName?: string;
}) => {
  const [transform, setTransform] = useState(
    "translate(-50%,-50%) rotateX(0deg)"
  );

  const open = () => {
    setTransform("translate(-50%,-50%) rotateX(40deg) scale(0.8)");
  };
  const close = () => {
    setTransform("translate(-50%,-50%) rotateX(0deg) scale(1)");
  };

  return (
    <Link
      href={href}
      className={cn(
        "relative group/pin z-50 cursor-pointer h-full w-full",
        containerClassName
      )}
      onMouseEnter={open}
      onMouseLeave={close}
      target={'_blank'}
    >
      <div
        style={{
          perspective: "1000px",
          transform: "rotateX(70deg) translateZ(0deg)",
        }}
        className="absolute left-1/2 top-1/2 ml-[0.09375rem] h-full w-full mt-4 -translate-x-1/2 -translate-y-1/2"
      >
        <div
          style={{
            transform: transform,
          }}
          className="absolute left-1/2 p-4 top-1/2 h-full w-full flex justify-start items-start  rounded-2xl  shadow-[0_8px_16px_rgb(0_0_0/0.4)] border border-white/[0.1] group-hover/pin:border-white/[0.2] transition duration-700 overflow-hidden"
        >
          <div className={cn("relative left-1/2 p-4 top-1/2 z-50 -translate-x-1/2 -translate-y-1/2", className)}>{children}</div>
        </div>
      </div>
      <PinPerspective title={title} />
    </Link>
  );
};

